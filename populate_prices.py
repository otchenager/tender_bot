def build_items() -> list[dict]:
    items = []
    for name, unit, total_cost, quantity, category in POSITIONS:
        items.append({
            "name": name,
            "unit": unit,
            "my_price": round(total_cost / quantity, 2),
            "category": category,
            "is_active": True,
            "is_unknown": False,
        })
    return items


def main():
    if not DATABASE_URL:
        print("DATABASE_URL not set in environment", file=sys.stderr)
        sys.exit(1)

    items = build_items()

    conn = psycopg2.connect(DATABASE_URL)
    try:
        conn.autocommit = False
        cur = conn.cursor()

        # The smeta legitimately repeats a handful of materials (same name/unit/price/
        # category) across sections, so dedup can't key on name alone — that would drop
        # real positions on the very first run. Instead compare against a snapshot of
        # existing rows as a multiset: a position is "already inserted" only if a matching
        # row from a *previous* run is still available to claim, which keeps the script
        # idempotent on reruns without undercounting on a fresh table.
        cur.execute("SELECT name, unit, my_price, category FROM price_items")
        existing = Counter(cur.fetchall())

        inserted = 0
        for item in items:
            key = (item["name"], item["unit"], item["my_price"], item["category"])
            if existing[key] > 0:
                existing[key] -= 1
                continue
            cur.execute(
                """
                INSERT INTO price_items (name, unit, my_price, category, is_active, is_unknown)
                VALUES (%(name)s, %(unit)s, %(my_price)s, %(category)s, %(is_active)s, %(is_unknown)s)
                """,
                item,
            )
            inserted += 1

        conn.commit()
        print(f"Inserted {inserted} rows ({len(items) - inserted} already existed, skipped).")
    except Exception:
        conn.rollback()
        raise
    finally:
        conn.close()


if __name__ == "__main__":
    main()
